package com.A06_String;

public class S09TrimmingStrWithTrim 
{
	public static void main(String[] args) 
	{
		String str1=" anant ";
				
		System.out.println("str1 without trim "+str1);
		System.out.println("length of str1 without trim "+str1.length());
		System.out.println("str1 with trim "+str1.trim());
		System.out.println("length of str1 with trim "+str1.trim().length());
	}
}
