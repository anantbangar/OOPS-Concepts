package com.A01_JavaBasics;

public class JB04Primitive_Data_Types 
{
	public static void main(String[] args) 
	{
		int num=10;
		char ch='C';
		float f=2.11f;
		double db=2.1232;
		boolean b=true;
		
		System.out.println("value of int is "+num);
		System.out.println("value of char is "+ch);
		System.out.println("value of float is "+f);
		System.out.println("value of double is "+db);
		System.out.println("value of boolean is "+b);
	}
}
